void decodeData(){
	for(int i=0; i<25; i++) {
		message[i] = (char)(rx_buffer[i]);
	}
	float x,y;
	char *token;

	token = strtok(message, " ");
	if (token != NULL) {
		x = atof(token);
	}

	token = strtok(NULL, " ");
	if (token != NULL) {
		y = atof(token);
	}

	token = strtok(NULL, " ");
	if (token != NULL) {
		strncpy(name, token, sizeof(name)-1);
		name[sizeof(name) - 1] = '\0';
	}
	x_int = (int) x;
	y_int = (int) y;
	update_servo_position(x_int, y_int);

	if(strcmp(old_name, name) == 0 && strcmp(old_name, "None") != 0) {
		lock_count++;
	}
	if(strcmp(old_name, "None") == 0) {
		reset_count++;
	} else {
		reset_count = 0;
	}
	if(lock_count == 1) {
		detect_start_time = HAL_GetTick();
	}
	strncpy(old_name, name, sizeof(name)-1);
}
